import java.util.Scanner;

public class Coord {
    private int x;
    private int y;

    public Coord(int x, int y){
        this.x = x;
        this.x = x;
    }

    public void setX(int x){
        this.x = x;
    }

    public void setY(int y){
        this.y = y;
    }

    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }
}
